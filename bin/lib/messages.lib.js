/////////////////////////////////////////////////////////////////////////////////////////////////////////
	const fs = require('fs');
	const __pathLib = require('path');
/////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////
	var LANGUAGES=[];
	var dir_element = fs.readdirSync(__path+'language');
	for (let i in dir_element) {
		if(fs.statSync(__path+'language/'+dir_element[i]).isFile() && __pathLib.extname(dir_element[i])=='.json'){
			LANGUAGES[__pathLib.basename(dir_element[i],'.json')] = require(__path+'language/'+dir_element[i]);
		}
	}
/////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////

module.exports=LANGUAGES;