	var {promisify} = require('util');
/////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////
	var promisifyConstruction=function(context,func){
		if(typeof func=='undefined'){
			func=context;
			context=null;
		}
		if(typeof context!='undefined'&&context!=null){
			return promisify(func).bind(context);
		}else{
			return promisify(func);
		}
	}

/////////////////////////////////////////////////////////////////////////////////////////////////////////
module.exports=promisifyConstruction;